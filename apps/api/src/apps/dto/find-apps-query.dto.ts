import { Type } from 'class-transformer';
import {
  IsIn,
  IsInt,
  IsOptional,
  IsString,
  Max,
  Min,
} from 'class-validator';
import { APP_CATEGORIES, AppCategory } from '../app.entity';

export class FindAppsQueryDto {
  @IsOptional()
  @IsString()
  @IsIn(APP_CATEGORIES)
  category?: AppCategory;

  @IsOptional()
  @IsInt()
  @Min(1)
  @Max(100)
  @Type(() => Number)
  limit?: number = 20;

  @IsOptional()
  @IsInt()
  @Min(0)
  @Type(() => Number)
  offset?: number = 0;
}
